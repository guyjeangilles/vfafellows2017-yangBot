var HTTPS = require('https');
var cool = require('cool-ascii-faces');

var botID = process.env.BOT_ID;

function respond() {
  var request = JSON.parse(this.req.chunks[0]),
      botRegexIterate = /^iterat/i;
	  botRegexDisrupt = /^disrupt/i;
	  botRegexYin = /^yin/i;
	  botRegexPlatform = /^platform/i;
	  botRegexAutomate = /^automat/i;
	  botRegexStartup = /^startup/i;
	  botRegexValue = /^value/i;
	  botRegexNetwork = /^network/i;
	  botRegexPivot = /^pivot/i;
	  botRegexSynergy = /^synerg/i;

  if(request.text && botRegexIterate.test(request.text)) {
    this.res.writeHead(200);
    postMessageIterate();
    this.res.end();
  }
  if (request.text && botRegexDisrupt.test(request.text)) {
	this.res.writeHead(200);
    postMessageDisrupt();
    this.res.end();
  }
  
  if(request.text && botRegexYin.test(request.text)) {
	this.res.writeHead(200);
    postMessageYin();
    this.res.end();
  } 
  
  if(request.text && botRegexPlatform.test(request.text)) {
	this.res.writeHead(200);
    postMessagePlatform();
    this.res.end();
  } 
  
  if(request.text && botRegexStartup.test(request.text)) {
	this.res.writeHead(200);
    postMessageStartup();
    this.res.end();
  }
  
  if(request.text && botRegexValue.test(request.text)) {
	this.res.writeHead(200);
    postMessageValue();
    this.res.end();
  }
  
  if(request.text && botRegexNetwork.test(request.text)) {
	this.res.writeHead(200);
    postMessageNetwork();
    this.res.end();
  }
  
  if(request.text && botRegexPivot.test(request.text)) {
	this.res.writeHead(200);
    postMessagePivot();
    this.res.end();
  }
  
  if(request.text && botRegexSynergy.test(request.text)) {
	this.res.writeHead(200);
    postMessageSynergy();
    this.res.end();
  }
  
  if(request.text && botRegexAutomate.test(request.text)) {
	this.res.writeHead(200);
    postMessageAutomate();
    this.res.end();
  } else {
    console.log("don't care");
    this.res.writeHead(200);
    this.res.end();
  }
}  

function postMessageIterate() {
  var botResponse, options, body, botReq;
  
  botResponse =' ITERATE';

  options = {
    hostname: 'api.groupme.com',
    path: '/v3/bots/post',
    method: 'POST'
  };

  body = {
    "bot_id" : botID,
    "text" : botResponse
  };

  console.log('sending ' + botResponse + ' to ' + botID);

  botReq = HTTPS.request(options, function(res) {
      if(res.statusCode == 202) {
        //neat
      } else {
        console.log('rejecting bad status code ' + res.statusCode);
      }
  });

  botReq.on('error', function(err) {
    console.log('error posting message '  + JSON.stringify(err));
  });
  botReq.on('timeout', function(err) {
    console.log('timeout posting message '  + JSON.stringify(err));
  });
  botReq.end(JSON.stringify(body));
}

